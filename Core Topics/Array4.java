// for each loop using in Array

public class Array4 {
    public static void main(String[] args)
    {
        int a[] = new int[]{3,2,1,6,7,5,4,10,9,8};
        System.out.println("The for each loop array elements are");
        for(int i:a)
        {
            System.out.println(i);
        }
        
    }
}
