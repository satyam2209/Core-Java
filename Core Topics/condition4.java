// Switch statement

public class condition4 {
    public static void main(String args[]) {
        int day = 6;
        switch (day) {
            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("Tuesday");
                break;
            case 3:
                System.out.println("Wednesday");
                break;
            case 4:
                System.out.println("Thrusday");
                break;
            default:
                System.out.println("Default case");
                break;

        }
    }
}
