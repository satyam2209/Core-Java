// Creating Strings

public class Strings1 {
    public static void main(String[] args)
    {
        String s1="Hello how are you";
        String s2="Welcome";
        System.out.println(s1);
        System.out.println(s2);
        // Concatenate Strings Using +
        System.out.println(s1+s2);
        System.out.println(s1+" "+s2);
    }
}
