public class Booleans {
    public static void main(String[] args)
    {
        boolean a = true;
        System.out.println(a);
        int x=1;
        int y=2;
        System.out.println(x>y);
        System.out.println(x<y);
        System.out.println(x==y);
        System.out.println(x!=y);
    }
}
