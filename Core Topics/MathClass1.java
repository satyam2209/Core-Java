// Methods of Math class

public class MathClass1 {
    public static void main(String[] args)
    {
        System.out.println("The maximum value is :"+Math.max(5, 20));
        System.out.println("The minimum value is :"+Math.min(5, 20));
        System.out.println("The square root of a number is :"+Math.sqrt(64));
        System.out.println("The absolute value of a number is :"+Math.abs(-4));
        System.out.println("The random number :"+Math.random());
        System.out.println("The random number :"+ (int)(Math.random()*51));
        System.out.println("The use of ceil function :"+Math.ceil(4.5));
        System.out.println("The use of floor function :"+Math.floor(4.55));       
    }
}
